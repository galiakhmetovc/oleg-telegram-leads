"""Decision trace and quality evaluation table definitions."""

from sqlalchemy import (
    Boolean,
    Column,
    DateTime,
    Float,
    Integer,
    JSON,
    MetaData,
    String,
    Table,
    Text,
)

metadata = MetaData()

decision_records_table = Table(
    "decision_records",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("decision_type", String(64), nullable=False),
    Column("entity_type", String(64), nullable=False),
    Column("entity_id", String(36), nullable=False),
    Column("dedupe_key", String(255), nullable=True),
    Column("source_message_id", String(36), nullable=True),
    Column("lead_event_id", String(36), nullable=True),
    Column("lead_cluster_id", String(36), nullable=True),
    Column("source_id", String(36), nullable=True),
    Column("classifier_version_id", String(36), nullable=True),
    Column("catalog_version_id", String(36), nullable=True),
    Column("catalog_hash", String(64), nullable=True),
    Column("prompt_hash", String(64), nullable=True),
    Column("prompt_version", String(80), nullable=True),
    Column("model", String(160), nullable=True),
    Column("settings_hash", String(64), nullable=True),
    Column("decision", String(64), nullable=False),
    Column("confidence", Float, nullable=True),
    Column("reason", Text, nullable=True),
    Column("input_json", JSON, nullable=True),
    Column("evidence_json", JSON, nullable=True),
    Column("output_json", JSON, nullable=True),
    Column("status", String(32), nullable=False),
    Column("created_by", String(160), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

evaluation_datasets_table = Table(
    "evaluation_datasets",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("dataset_key", String(160), nullable=False),
    Column("name", String(255), nullable=False),
    Column("dataset_type", String(64), nullable=False),
    Column("description", Text, nullable=True),
    Column("status", String(32), nullable=False),
    Column("created_by", String(160), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
    Column("updated_at", DateTime(timezone=True), nullable=False),
)

evaluation_cases_table = Table(
    "evaluation_cases",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("evaluation_dataset_id", String(36), nullable=False),
    Column("source_message_id", String(36), nullable=True),
    Column("lead_cluster_id", String(36), nullable=True),
    Column("lead_event_id", String(36), nullable=True),
    Column("feedback_event_id", String(36), nullable=True),
    Column("source_id", String(36), nullable=True),
    Column("message_text", Text, nullable=True),
    Column("context_json", JSON, nullable=True),
    Column("expected_decision", String(32), nullable=True),
    Column("expected_category_id", String(36), nullable=True),
    Column("expected_catalog_item_ids_json", JSON, nullable=True),
    Column("expected_reason_code", String(80), nullable=True),
    Column("expected_notification_policy", String(32), nullable=True),
    Column("expected_cluster_behavior", String(32), nullable=True),
    Column("expected_crm_candidate_json", JSON, nullable=True),
    Column("label_source", String(32), nullable=False),
    Column("created_by", String(160), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
    Column("updated_at", DateTime(timezone=True), nullable=False),
)

evaluation_runs_table = Table(
    "evaluation_runs",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("evaluation_dataset_id", String(36), nullable=False),
    Column("run_type", String(64), nullable=False),
    Column("classifier_version_id", String(36), nullable=True),
    Column("catalog_hash", String(64), nullable=True),
    Column("prompt_hash", String(64), nullable=True),
    Column("model", String(160), nullable=True),
    Column("settings_hash", String(64), nullable=True),
    Column("status", String(32), nullable=False),
    Column("started_at", DateTime(timezone=True), nullable=False),
    Column("finished_at", DateTime(timezone=True), nullable=True),
    Column("metrics_json", JSON, nullable=True),
    Column("error", Text, nullable=True),
    Column("created_by", String(160), nullable=False),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

evaluation_results_table = Table(
    "evaluation_results",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("evaluation_run_id", String(36), nullable=False),
    Column("evaluation_case_id", String(36), nullable=False),
    Column("decision_record_id", String(36), nullable=True),
    Column("actual_decision", String(32), nullable=True),
    Column("actual_category_id", String(36), nullable=True),
    Column("actual_catalog_item_ids_json", JSON, nullable=True),
    Column("actual_notification_policy", String(32), nullable=True),
    Column("actual_cluster_behavior", String(32), nullable=True),
    Column("actual_crm_candidate_json", JSON, nullable=True),
    Column("passed", Boolean, nullable=False),
    Column("failure_type", String(64), nullable=True),
    Column("details_json", JSON, nullable=True),
    Column("created_at", DateTime(timezone=True), nullable=False),
)

quality_metric_snapshots_table = Table(
    "quality_metric_snapshots",
    metadata,
    Column("id", String(36), primary_key=True),
    Column("scope", String(64), nullable=False),
    Column("scope_id", String(160), nullable=True),
    Column("period_start", DateTime(timezone=True), nullable=True),
    Column("period_end", DateTime(timezone=True), nullable=True),
    Column("precision", Float, nullable=True),
    Column("recall", Float, nullable=True),
    Column("f1", Float, nullable=True),
    Column("false_positive_count", Integer, nullable=False),
    Column("false_negative_count", Integer, nullable=False),
    Column("maybe_count", Integer, nullable=False),
    Column("maybe_resolution_rate", Float, nullable=True),
    Column("high_value_precision", Float, nullable=True),
    Column("retro_precision", Float, nullable=True),
    Column("telegram_notification_precision", Float, nullable=True),
    Column("catalog_candidate_accept_rate", Float, nullable=True),
    Column("catalog_candidate_reject_rate", Float, nullable=True),
    Column("feedback_count", Integer, nullable=False),
    Column("metrics_json", JSON, nullable=True),
    Column("created_at", DateTime(timezone=True), nullable=False),
)
